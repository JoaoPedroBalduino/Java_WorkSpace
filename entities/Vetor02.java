package entities;

public class Vetor02 {
// Cod30_Vetor02
	
	private String name;
	private double price;
	
//Construtor--------------------------------------------------------------------------------
	
	public Vetor02(String name, double price) {
		this.name = name;
		this.price = price;
	}

//Get e Set--------------------------------------------------------------------------------


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public double getPrice() {
		return price;
	}


	public void setPrice(double price) {
		this.price = price;
	}
	
	
}
